package fundamentos;

public class Inferencia {

	public static void main(String[] args) {

		double a = 4.5; // declaração e inicialização
		System.out.println(a); // usada

		var b = 4.5; // declaração e inicialização
		System.out.println(b);// usada

		var c = "Udemy"; // declaração e inicialização
		System.out.println(c);// usada

		c = "Outro"; // declaração e inicialização
		System.out.println(c);// usada

		double d; // variável foi declarada
		d = 123.65; // variável foi inicializada
		System.out.println(d); // usada

		var e = 123.45; // variável foi declarada
		System.out.println(e); // usada
	}

}
