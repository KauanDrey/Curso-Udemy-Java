package estruturasDeControle;

public class WhileDeterminado {

	
	public static void main(String[] args) {
		
		//While fica repetindo as expressões verdadeiras até que vire falso
		
		int contador = 1; 
		
		while(contador <= 10) {
		System.out.printf("Number: %d\n", contador);
		contador++;
		}
		
		
	}
}
